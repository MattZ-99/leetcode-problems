class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        int m = obstacleGrid.size();
        int n = obstacleGrid[0].size();
        if(obstacleGrid[0][0] == 1) return 0;
        obstacleGrid[0][0] = 1;
        for (int a=1; a<m; a++) obstacleGrid[a][0] = obstacleGrid[a][0] ? 0 : obstacleGrid[a-1][0];
        for (int b=1; b<n; b++) obstacleGrid[0][b] = obstacleGrid[0][b] ? 0 : obstacleGrid[0][b-1];

        for(int a=1; a<m; a++)
            for(int b=1; b<n; b++)
                obstacleGrid[a][b] = obstacleGrid[a][b] ? 0 : obstacleGrid[a-1][b] + obstacleGrid[a][b-1];

        return obstacleGrid[m-1][n-1];
    }
};

